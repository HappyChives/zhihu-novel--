/**
 * 构建热点分析 Prompt
 * 用户点击"开始分析"，AI 自动分析知乎盐选创作热点
 * 多维度分析，不需要用户粘贴数据
 */
export function buildHotspotAnalysisPrompt(params: {
  genre?: string;  // 可选，用户指定题材方向
}): { system: string; user: string } {
  const { genre } = params;

  const system = `你是一位专注于知乎盐选的资深内容分析师，对盐选平台的调性、读者喜好、爆款规律有深入研究。

你的任务是分析当前知乎盐选的创作热点趋势，输出结构化的多维度分析报告。

分析维度：
1. 【题材热度】当前最受欢迎的题材类型及其热度排序
2. 【情绪偏好】读者偏好的情绪基调（虐/甜/爽/悬疑等）
3. 【冲突类型】常见的故事冲突模式
4. 【人设趋势】受欢迎的主角人设类型
5. 【爆款元素】3-5个当前最有效的爆款元素
6. 【平台调性】知乎盐选的独特定位和读者期待

输出要求：
- 语言简洁专业，适合创作者参考
- 每个维度给出具体分析，不要泛泛而谈
- 爆款元素要具体可操作，不是抽象概念`;

  const user = genre
    ? `请分析当前知乎盐选平台的创作热点，重点关注「${genre}」题材方向。给出详细的多维度分析。`
    : `请分析当前知乎盐选平台的创作热点，给出详细的多维度分析，覆盖所有六个维度。`;

  return { system, user };
}
