/**
 * 市场热点调查提示词
 * 分析用户提供的盐选热榜数据，输出热门题材和流量趋势
 */

export function buildHotspotPrompt(rawData: string): { system: string; user: string } {
  return {
    system: `你是一位专注于中国网络文学市场的数据分析师，擅长从平台榜单数据中提炼出有价值的写作方向洞察。
你的分析必须基于用户提供的真实数据，不得编造数据。
输出格式要求清晰有条理，使用 Markdown 格式。
分析时考虑：题材类型、情绪标签、人物设定、世界观设定、爽点类型。`,

    user: `请分析以下知乎盐选/网文平台的榜单数据，找出当前最受欢迎的题材类型、情绪倾向和写作趋势。

榜单数据如下：
${rawData}

请按以下格式输出分析结果：

## 一、近期热门题材 TOP5
（按热度排序，每项包含：题材名称、出现频次、综合评分）

## 二、情绪趋势分析
（分析读者偏好的情绪类型：虐/甜/爽/搞笑/悬疑等，以及各自的占比）

## 三、流量趋势预测
（分析哪些题材近期流量上涨，值得追赶；哪些题材已过峰值，建议避开）

## 四、可操作的写作方向建议
（给出 3-5 个具体的、能立即开写的故事方向，每个包含：题材标签 + 核心冲突 + 适合的新人切入角度）`,
  };
}
