/**
 * 选题确定提示词
 * 将灵感碎片转化为可开写的选题
 */

export function buildTopicPrompt(inspirationText: string): { system: string; user: string } {
  return {
    system: `你是一位专注于知乎盐选短篇的资深编辑，擅长从故事灵感中提炼出最适合投稿盐选的选题方向。
你产出的选题必须满足：
1. 知乎盐选调性：短句节奏、快节奏冲突、情绪强烈、反转有力
2. 有市场验证：要么踩中热门题材，要么有独特视角
3. 商业价值高：能引发读者强烈情绪共鸣（虐/甜/爽），付费意愿强
4. 可落地写：给了创作者就能立即动笔的程度

每个选题包含：一句话核心冲突句（知乎风格）、适合的盐选分类标签、预估读者情绪走向、标题方向建议。`,

    user: `以下是一组故事灵感碎片，请将它们精炼成 3-5 个最适合在知乎盐选发表的选题：

${inspirationText}

请按以下格式输出（每个选题之间用分隔线隔开）：

---
### 选题 [编号]

**核心冲突句（知乎风格，20字以内，要有画面感）：**
[一句话描述核心冲突]

**盐选分类标签：**
[给出一组标签，如：豪门恩怨/现代言情/重生/悬疑等]

**情绪走向：**
[描述读者的情绪体验路径，如：憋屈→反转→爽]

**推荐标题方向（3个）：**
1. [标题方向1]
2. [标题方向2]
3. [标题方向3]
---`,
  };
}
