/**
 * 大纲生成提示词
 * 基于选题生成完整的四幕结构大纲
 */

export function buildOutlinePrompt(
  topicText: string,
  selectedTopic?: string
): { system: string; user: string } {
  const topic = selectedTopic || topicText;

  return {
    system: `你是一位专业的故事架构师，擅长使用"起承转合"四幕结构为短篇小说搭建完整大纲。
你的大纲必须：
1. 严格遵循四幕结构：起（建立世界+冲突）、承（冲突升级）、转（最大反转/转折）、合（高潮+收尾）
2. 每幕有明确的戏剧任务、关键情节点和字数分配建议
3. 人物弧光清晰：主角从困境到改变的转变路径明确
4. 知乎盐选节奏：适合 8000-20000 字的短篇节奏，情节点紧凑

大纲包含：四幕结构详情 + 人物弧光建议 + 章节划分建议 + 关键场景清单。`,

    user: `请为以下选题搭建完整的四幕结构大纲：

${topic}

请按以下格式输出：

## 第一幕：起（占全文约 15%，建立世界与冲突）
**戏剧任务：** [这一幕要完成什么？]
**关键情节点：**
- [情节点1]
- [情节点2]
- [情节点3]
**场景清单：**
- [场景1：描写...]
- [场景2：描写...]
**字数建议：** 约 1200-3000 字

## 第二幕：承（占全文约 35%，冲突持续升级）
**戏剧任务：** [这一幕要完成什么？]
**关键情节点：**
- [情节点1]
- [情节点2]
- [情节点3]
- [情节点4]
**场景清单：**
- [场景...]
**字数建议：** 约 2800-7000 字

## 第三幕：转（占全文约 30%，最大反转或转折）
**戏剧任务：** [这一幕要完成什么？]
**关键情节点：**
- [情节点1]
- [情节点2]
- [情节点3]
**场景清单：**
- [场景...]
**字数建议：** 约 2400-6000 字

## 第四幕：合（占全文约 20%，高潮与收尾）
**戏剧任务：** [这一幕要完成什么？]
**关键情节点：**
- [情节点1]
- [情节点2]
**场景清单：**
- [场景...]
**字数建议：** 约 1600-4000 字

## 人物弧光
**主角：**
- 起点状态：[描述]
- 转变触发事件：[事件]
- 终点状态：[描述]

**反派/对照组：**
- 动机：[描述]
- 与主角的核心矛盾：[描述]

## 总字数建议
[给出建议的总字数范围]`,
  };
}
